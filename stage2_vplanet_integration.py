    # ── Panel 3: Atmospheric Escape ──────────────────────────────────────
    ax = axes[2]
    if not FALLBACK and 'Time_Gyr' in planet_df.columns:
        t = planet_df['Time_Gyr']
        if 'EnvelopeMass_Mearth' in planet_df.columns:
            ax.semilogy(t, planet_df['EnvelopeMass_Mearth'].clip(1e-12),
                        'green', lw=2, label='H envelope mass')
            ax.axhline(PLANET['envelope_mass_mearth'], color='k', ls='--', lw=1,
                       label=f'Initial: {PLANET["envelope_mass_mearth"]:.0e} Mearth')
        if 'HEscapeRegime' in planet_df.columns:
            regimes = planet_df['HEscapeRegime']
            ax3 = ax.twinx()
            ax3.plot(t, regimes, 'orange', lw=1, alpha=0.6, label='Escape regime (0=none)')
            ax3.set_ylabel('Escape Regime', color='orange', fontsize=8)
            ax3.tick_params(axis='y', colors='orange')
    else:
        # Analytical escape estimate
        t_gyr  = np.linspace(0.1, 7.6, 500)
        M_init = PLANET['envelope_mass_mearth']
        # Simple mass loss: dM/dt proportional to FXUV
        L_sat  = 1e-3 * 5.22e-4 * 3.828e33
        L_xuv  = np.where(t_gyr < 1.0, L_sat, L_sat * (t_gyr/1.0)**(-1.23))
        a_cm   = 0.02928 * 1.496e13
        F_xuv  = L_xuv / (4*np.pi*a_cm**2)
        # Energy-limited escape: Mdot = eps * pi * R^2 * FXUV / (G*M/R)
        R_p  = 0.920 * 6.371e8          # cm
        M_p  = 0.692 * 5.972e27         # g
        eps  = PLANET['atm_esc_eff']
        G    = 6.674e-8                  # cgs
        Mdot = eps * np.pi * R_p**2 * F_xuv / (G * M_p / R_p)   # g/s
        dt_s = np.gradient(t_gyr) * 1e9 * 3.156e7
        M_lost = np.cumsum(Mdot * dt_s) / 5.972e27               # Mearth
        M_env  = np.maximum(M_init - M_lost, 1e-12)
        ax.semilogy(t_gyr, M_env, 'green', lw=2, label='H envelope (analytical)')
        ax.axhline(M_init, color='k', ls='--', lw=1,
                   label=f'Initial: {M_init:.0e} Mearth')

    ax.axvline(7.6, color='green', ls=':', lw=1.5, label='Current age')
    ax.set_xlabel('Stellar Age (Gyr)')
    ax.set_ylabel('H Envelope Mass (Mearth)')
    ax.set_title('Atmospheric Escape (H envelope)\nAtmEsc module: Barnes+2019', fontsize=10)
    ax.legend(fontsize=7)

