# ── TRAPPIST-1 System Parameters ────────────────────────────────────────────
# All from NASA Exoplanet Archive and published literature
STAR = dict(
    mass_msun    = 0.0898,    # Solar masses
    lum_lsun     = 5.22e-4,   # Solar luminosities
    age_gyr      = 7.6,       # Current age (Gyr)
    rot_per_days = 3.30,      # Rotation period
    sat_xuv_frac = 1.0e-3,    # XUV saturation fraction (Lx/Lbol)
    sat_xuv_gyr  = 1.0,       # XUV saturation duration (Gyr)
    xuv_beta     = -1.23,     # XUV power-law decay exponent post-saturation
    flare_E_min_erg = 1e30,   # Minimum flare energy (erg) from Vida+2017
    flare_E_max_erg = 5e33,   # Maximum flare energy (erg)
)

PLANET = dict(
    mass_mearth  = 0.692,     # Earth masses
    radius_rearth= 0.920,     # Earth radii
    semi_au      = 0.02928,   # Semi-major axis (AU)
    ecc          = 0.0,
    # Initial H envelope for escape modelling -- represents a primordial thin
    # secondary atmosphere (0.001 Earth masses ~ 6e21 kg ~ 1% of Earth's atm mass)
    envelope_mass_mearth = 1e-3,
    atm_esc_eff  = 0.15,      # XUV absorption efficiency (Barnes+2019)
)

# Conversion factors
ERG_TO_J = 1e-7
J_TO_ERG  = 1e7
AU_CM = 1.496e13


# ===========================================================================
# SECTION 1: Build VPLanet Input Files
# ===========================================================================
def write_vplanet_inputs(tmpdir, start_age_gyr=0.1, stop_age_gyr=7.6,
                         output_interval_myr=10.0):
    """
    Write VPLanet primary input file and two body files (star + planet)
    into tmpdir. Returns paths to all three files.

    Simulation: TRAPPIST-1 stellar+flare evolution + TRAPPIST-1e atmospheric
    escape from start_age_gyr to stop_age_gyr.

    Unit convention: negative values = use the custom unit shown in docs.
      dMass (star)    : negative = solar masses
      dMass (planet)  : negative = Earth masses
      dSemi           : negative = AU
      dAge            : negative = Gyr
      dLuminosity     : negative = solar luminosities
      dEnvelopeMass   : negative = Earth masses
      dFlareMinEnergy : positive = Joules (SI)
      dFlareMaxEnergy : positive = Joules (SI)
    """
    stop_yr  = stop_age_gyr  * 1e9
    start_yr = start_age_gyr * 1e9
    out_yr   = output_interval_myr * 1e6

    # ── Primary file ────────────────────────────────────────────────────────
    vpl_in = textwrap.dedent(f"""\
        # VPLanet primary input: TRAPPIST-1e Digital Twin
        # Generated by stage2_vplanet_integration.py
        sSystemName   trappist1e_twin
        iVerbose      0
        bOverwrite    1
        saBodyFiles   trappist1_star.in trappist1e_planet.in
        dStopTime     {stop_yr:.3e}       # years ({stop_age_gyr} Gyr)
        dOutputTime   {out_yr:.3e}        # years (every {output_interval_myr:.0f} Myr)
        Units         Years
    """)

    # ── Star: STELLAR + FLARE modules ───────────────────────────────────────
    # Using sFlareFFD DAVENPORT: auto-calibrates FFD from mass and age
    # via Davenport+2019 ApJ 871 scaling relations (calibrated on Kepler M-dwarfs)
    E_min_J = STAR['flare_E_min_erg'] * ERG_TO_J   # 1e23 J
    E_max_J = STAR['flare_E_max_erg'] * ERG_TO_J   # 5e26 J

    star_in = textwrap.dedent(f"""\
        # TRAPPIST-1 stellar input
        sName            trappist1
        saModules        stellar flare
        dMass            -{STAR['mass_msun']}         # Solar masses
        dAge             -{start_age_gyr}             # Gyr (initial simulation age)
        dLuminosity      -{STAR['lum_lsun']}          # Solar luminosities
        dSatXUVFrac      {STAR['sat_xuv_frac']}       # Lx/Lbol saturation (Johnstone+2021)
        dSatXUVTime      -{STAR['sat_xuv_gyr']}       # Gyr saturation duration
        dXUVBeta         {STAR['xuv_beta']}            # Post-saturation power law
        sFlareFFD        DAVENPORT                    # Davenport+2019 FFD scaling
        dFlareMinEnergy  {E_min_J:.3e}                # J (= 1e30 erg, Vida+2017 TRAPPIST-1)
        dFlareMaxEnergy  {E_max_J:.3e}                # J (= 5e33 erg)
        saOutputOrder    Time LXUVFlare LXUVTot LXUVFrac Luminosity RotPer
    """)

    # ── Planet: AtmEsc module ────────────────────────────────────────────────
    # Models XUV-driven atmospheric escape (energy-limited; Barnes+2019)
    planet_in = textwrap.dedent(f"""\
        # TRAPPIST-1e planetary input
        sName            trappist1e
        saModules        atmesc
        dMass            -{PLANET['mass_mearth']}      # Earth masses
        dRadius          -{PLANET['radius_rearth']}    # Earth radii
        dSemi            -{PLANET['semi_au']}          # AU
        dEcc             {PLANET['ecc']}
        dEnvelopeMass    -{PLANET['envelope_mass_mearth']}  # Earth masses (initial H envelope)
        dAtmXAbsEffH     {PLANET['atm_esc_eff']}       # XUV absorption efficiency
        saOutputOrder    Time FXUV EnvelopeMass OxygenMass HEscapeRegime
    """)

    paths = {}
    for fname, content in [('vpl.in', vpl_in),
                            ('trappist1_star.in', star_in),
                            ('trappist1e_planet.in', planet_in)]:
        p = os.path.join(tmpdir, fname)
        with open(p, 'w') as f:
            f.write(content)
        paths[fname] = p

    return paths['vpl.in']


# ===========================================================================
# SECTION 2: Run VPLanet and Parse Output
# ===========================================================================
def run_vplanet_simulation(start_age_gyr=0.1, stop_age_gyr=7.6,
                           output_interval_myr=10.0):
    """
    Execute VPLanet STELLAR+FLARE+AtmEsc simulation for TRAPPIST-1 system.
    Returns (star_df, planet_df) pandas DataFrames with time-series outputs.
    """
    print("\n" + "="*65)
    print("VPLanet: Running STELLAR + FLARE + AtmEsc simulation")
    print("="*65)
    print(f"  System:      TRAPPIST-1 / TRAPPIST-1e")
    print(f"  Time range:  {start_age_gyr} Gyr -- {stop_age_gyr} Gyr")
    print(f"  Output cadence: every {output_interval_myr:.0f} Myr")
    print(f"  Modules:     STELLAR + FLARE (Davenport+2019) + AtmEsc (Barnes+2019)")

    tmpdir = tempfile.mkdtemp(prefix='vplanet_t1e_')
    vpl_path = write_vplanet_inputs(tmpdir, start_age_gyr, stop_age_gyr,
                                    output_interval_myr)

    try:
        out = vplanet.run(vpl_path, quiet=True, units=False)
    except vplanet.VPLANETError as e:
        print(f"  VPLanet run error: {e}")
        print("  Falling back to analytical XUV model (no VPLanet output).")
        return None, None

    # ── Extract star outputs ─────────────────────────────────────────────
    star_body = out.trappist1
    star_data = {}

    for attr in dir(star_body):
        if attr.startswith('_'):
            continue
        try:
            val = getattr(star_body, attr)
            if hasattr(val, '__len__') and len(val) > 1:
                star_data[attr] = np.array(val)
        except Exception:
            pass

    # ── Extract planet outputs ───────────────────────────────────────────
    planet_body = out.trappist1e
    planet_data = {}

    for attr in dir(planet_body):
        if attr.startswith('_'):
            continue
        try:
            val = getattr(planet_body, attr)
            if hasattr(val, '__len__') and len(val) > 1:
                planet_data[attr] = np.array(val)
        except Exception:
            pass

    # Build DataFrames -- coerce all arrays to the same length
    n_star = len(star_data.get('Time', []))
    star_df = pd.DataFrame(
        {k: v[:n_star] for k, v in star_data.items() if len(v) >= n_star}
    ) if star_data else pd.DataFrame()

    n_planet = len(planet_data.get('Time', []))
    planet_df = pd.DataFrame(
        {k: v[:n_planet] for k, v in planet_data.items() if len(v) >= n_planet}
    ) if planet_data else pd.DataFrame()

    # ── Post-process units ───────────────────────────────────────────────
    # Convert time from years to Gyr
    if 'Time' in star_df.columns:
        star_df['Time_Gyr'] = star_df['Time'] / 1e9
    if 'Time' in planet_df.columns:
        planet_df['Time_Gyr'] = planet_df['Time'] / 1e9

    # LXUVFlare and LXUVTot are in Watts in VPLanet output (SI)
    # Convert to erg/s for comparison with Stage 2 literature values
    for col in ['LXUVFlare', 'LXUVTot', 'LXUVFrac', 'Luminosity']:
        if col in star_df.columns:
            # VPLanet returns in SI (Watts); note LXUVFrac is dimensionless
            if col != 'LXUVFrac':
                star_df[f'{col}_ergs'] = star_df[col] * J_TO_ERG
            else:
                star_df[f'{col}_ergs'] = star_df[col]

    # FXUV in planet output: W/m^2 → erg/s/cm^2
    if 'FXUV' in planet_df.columns:
        planet_df['FXUV_ergs_cm2'] = planet_df['FXUV'] * 1e3   # W/m^2 → erg/s/cm^2

    # Cumulative XUV fluence (erg/cm^2) -- integrate FXUV over time
    if 'FXUV_ergs_cm2' in planet_df.columns and 'Time' in planet_df.columns:
        dt_s = np.diff(planet_df['Time'].values) * 365.25 * 86400   # yr → s
        fxuv = planet_df['FXUV_ergs_cm2'].values[:-1]
        planet_df['CumulXUV_ergs_cm2'] = np.append(
            [0.0], np.cumsum(fxuv * dt_s))

    # EnvelopeMass: in kg in VPLanet; convert to Earth masses
    if 'EnvelopeMass' in planet_df.columns:
        M_EARTH_KG = 5.972e24
        planet_df['EnvelopeMass_Mearth'] = planet_df['EnvelopeMass'] / M_EARTH_KG

    print(f"  Star output columns:   {list(star_df.columns[:8])}")
    print(f"  Planet output columns: {list(planet_df.columns[:8])}")
    print(f"  Time steps: {len(star_df)} (star) / {len(planet_df)} (planet)")

    # Save CSVs
    star_df.to_csv(f"{OUTPUT_DIR}/vplanet_star_evolution.csv", index=False)
    planet_df.to_csv(f"{OUTPUT_DIR}/vplanet_planet_evolution.csv", index=False)
    print(f"  Saved: {OUTPUT_DIR}/vplanet_star_evolution.csv")
    print(f"  Saved: {OUTPUT_DIR}/vplanet_planet_evolution.csv")

    return star_df, planet_df


