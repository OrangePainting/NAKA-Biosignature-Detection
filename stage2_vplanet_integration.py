"""
===========================================================================
TRAPPIST-1e Digital Twin -- Stage 2 Extension: VPLanet Integration
===========================================================================

Uses the VPLanet (Virtual Planetary Laboratory Planet Simulator) package
to model long-timescale (100 Myr - 7.6 Gyr) XUV/UV evolution and
atmospheric escape for the TRAPPIST-1 system.

Modules used:
  STELLAR  -- Stellar luminosity and XUV evolution (Barnes+2020)
  FLARE    -- Flare frequency distribution and XUV contribution (Davenport+2019)
  AtmEsc   -- Atmospheric escape driven by XUV bombardment (Barnes+2019)

What VPLanet adds to Stage 2:
  1. Long-timescale cumulative XUV fluence at TRAPPIST-1e (Gyr context)
  2. Fraction of total XUV from flares vs. quiescent stellar emission
  3. Atmospheric escape history: can TRAPPIST-1e retain a secondary N2 atm?
  4. Geological boundary conditions for the photochemical Stage 2 model

What VPLanet does NOT provide (honest limitations):
  - Full 3D atmospheric dynamics (would need CESM/WACCM or Met Office UM)
  - Short-timescale photochemistry (handled by our Chapman+HOx ODE in Stage 2)
  - Transmission spectra (handled by petitRADTRANS in Stage 3)

VPLanet is a 1D planetary evolution code. The 3D atmospheric models from
Chen+2021 (CESM1/WACCM) and Ridgway+2023 (Met Office Unified Model) are
not pip-installable and require dedicated HPC resources.

References:
  Barnes+2020 PASP 132 (VPLanet paper)
  Davenport+2019 ApJ 871 (FFD scaling relations used by FLARE module)
  Barnes+2019 ApJ 871 (AtmEsc XUV escape model)
  Johnstone+2021 A&A (XUV-driven escape for TRAPPIST-1e)
  Bolmont+2017 MNRAS (TRAPPIST-1e habitability evolution)
"""

