# SECTION 3: Compute Key Derived Quantities
# ===========================================================================
def compute_xuv_context(star_df, planet_df):
    """
    Derive scientifically useful quantities from VPLanet output.
    Returns a summary dict for use in plots and the Stage 2 photochem model.
    """
    ctx = {}

    if star_df is None or len(star_df) == 0:
        print("  No VPLanet star data; computing analytical XUV fallback.")
        return _analytical_xuv_context()

    # ── Current-age values (last row ~ 7.6 Gyr) ─────────────────────────
    final = star_df.iloc[-1]
    ctx['L_xuv_flare_now_ergs']  = final.get('LXUVFlare_ergs', np.nan)
    ctx['L_xuv_total_now_ergs']  = final.get('LXUVTot_ergs', np.nan)
    ctx['L_xuv_frac_now']        = final.get('LXUVFrac', np.nan)
    ctx['lum_now_lsun']          = final.get('Luminosity', np.nan) / 3.828e33 \
                                   if 'Luminosity' in star_df.columns else np.nan

    # ── Flare fraction of XUV at current age ────────────────────────────
    if 'LXUVFlare_ergs' in star_df.columns and 'LXUVTot_ergs' in star_df.columns:
        ctx['flare_xuv_frac_series'] = (star_df['LXUVFlare_ergs'] /
                                        star_df['LXUVTot_ergs'].clip(1e-30))
        ctx['flare_xuv_frac_now'] = float(ctx['flare_xuv_frac_series'].iloc[-1])
    else:
        ctx['flare_xuv_frac_now'] = np.nan

    # ── Cumulative XUV fluence over system lifetime ──────────────────────
    if 'CumulXUV_ergs_cm2' in planet_df.columns:
        ctx['cumul_xuv_fluence_ergs_cm2'] = float(planet_df['CumulXUV_ergs_cm2'].iloc[-1])
        ctx['log10_cumul_xuv'] = np.log10(max(ctx['cumul_xuv_fluence_ergs_cm2'], 1e-30))
    else:
        ctx['cumul_xuv_fluence_ergs_cm2'] = np.nan

    # ── Atmospheric escape summary ───────────────────────────────────────
    if 'EnvelopeMass_Mearth' in planet_df.columns:
        init_mass  = float(planet_df['EnvelopeMass_Mearth'].iloc[0])
        final_mass = float(planet_df['EnvelopeMass_Mearth'].iloc[-1])
        ctx['envelope_init_mearth']   = init_mass
        ctx['envelope_final_mearth']  = final_mass
        ctx['envelope_frac_retained'] = final_mass / max(init_mass, 1e-30)
        ctx['envelope_lost_pct']      = (1 - ctx['envelope_frac_retained']) * 100
    else:
        ctx['envelope_final_mearth']  = np.nan
        ctx['envelope_lost_pct']      = np.nan

    return ctx


def _analytical_xuv_context():
    """
    Fallback: analytical XUV model using Johnstone+2021 / Ribas+2005 scaling.
    Used if VPLanet fails. Provides same interface as compute_xuv_context().
    """
    L_bol_erg = 5.22e-4 * 3.828e33       # erg/s
    # Saturation phase: Lx/Lbol = 1e-3 for ~1 Gyr
    L_xuv_sat  = 1e-3 * L_bol_erg         # erg/s
    # Post-saturation at 7.6 Gyr (power law, beta=-1.23):
    t_sat = 1.0   # Gyr
    t_now = 7.6
    L_xuv_now  = L_xuv_sat * (t_now/t_sat)**(-1.23)

    # Cumulative XUV fluence at TRAPPIST-1e
    a_cm = 0.02928 * 1.496e13
    # Integrate: sat phase (0.1-1 Gyr) + post-sat (1-7.6 Gyr)
    F_sat  = L_xuv_sat  / (4*np.pi*a_cm**2) * 0.9e9 * 3.156e7   # erg/cm^2
    # Post-sat integral of t^-1.23 from 1 to 7.6 Gyr
    beta, t1, t2 = -1.23, 1.0, 7.6
    integral = (t2**(beta+1) - t1**(beta+1)) / (beta+1)  # Gyr^(beta+1)
    F_postsat = (L_xuv_sat / (4*np.pi*a_cm**2)) * integral * 1e9 * 3.156e7
    cumul = F_sat + abs(F_postsat)

    return dict(
        L_xuv_flare_now_ergs  = 0.05 * L_xuv_now,   # ~5% from flares (estimate)
        L_xuv_total_now_ergs  = L_xuv_now,
        L_xuv_frac_now        = L_xuv_now / L_bol_erg,
        lum_now_lsun          = 5.22e-4,
        flare_xuv_frac_now    = 0.05,
        cumul_xuv_fluence_ergs_cm2 = cumul,
        log10_cumul_xuv       = np.log10(cumul),
        envelope_init_mearth  = 1e-3,
        envelope_final_mearth = np.nan,
        envelope_lost_pct     = np.nan,
        note = 'analytical fallback (VPLanet unavailable)'
    )


